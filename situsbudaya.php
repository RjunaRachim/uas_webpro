      <h2>Situs Kebudayaan Kabupaten Blitar</h2>
    <p> &nbsp;&nbsp;&nbsp;&nbsp; Kabupaten Blitar memiliki kekayaan obyek wisata yang dapat diandalkan dan memiliki
      peluang untuk
      dikembangkan di
      masa depan. Hal tersebut mengingat terdapat bermacam-macam jenis obyek wisata yang mempunyai daya tarik khusus.
      Selain itu, obyek wisata yang tersebar di Kabupaten Blitar dapat bersinergi dengan obyek wisata di daerah lain
      seperti Kabupaten Kediri, Kota Blitar dan Kabupaten Malang. Berbagai jenis obyek wisata yang terdiri dari wisata
      sejarah , wisata alam, wisata rekreasi dan wisata budaya.</p>
  
    <p> &nbsp;&nbsp;&nbsp;&nbsp; Kabupaten Blitar sering juga di sebut “daerah seribu candi” yang di bangun mulai Dinasti
      Singasari, Dinasti
      Majapahit sampai Dinasti Kediri. Candi penataran di Kelurahan Penataran Kecamatan Nglegok merupakan Komplek
      percandian terbesar di Jawa Timur yang di bangun selama tiga generasi. Selain itu peninggalan purbakala lainnya yang
      sangat penting adalah prasasti merupakan cacatan sejarah yang paling orisinil dan otentik. Dari sekian prasasti yang
      ada (sebagian telah terkubur material gunung berapi) prasasti Balitar 1 yang di keluarkan oleh Batara Jayanegara
      pada tahun 1246 Caka mempunyai nilai penting karena berkaitan dengan awal berdirinya Kabupaten Blitar. Prasasti
      tersebut sebagai dokumen resmi pemberian hak swatantra (otonomi) oleh Bathara Jayanegara kepada rakyat Blitar di
      bawah naungan Negara oleh kesatuan Majapahit. Prasasti Balitar 1 berangka Tahun 1246 Caka, dengan tanggal 05 Agustus
      1324 Masehi yang selanjutnya di tetapkan sebagai Hari Jadi Kabupaten Blitar.</p>
  
