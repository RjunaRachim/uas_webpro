        <p>
        Apakah teman teman pernah mendengar istilah kebudayaan? Tentu sering
        bukan? Apalagi sebagai warga negara Indonesia yang terkenal karena
        memiliki kebudayaan yang beragam dari Sabang sampai Merauke. Secara
        umum, budaya atau kebudayaan merupakan cara hidup yang berkembang dan
        dimiliki oleh bersama serta diwariskan dari generasi ke generasi
        berikutnya. Contohnya seperti adat ‘ngunduh mantu’ yang ada di Jawa yang
        akan dilaksanakan ketika seseorang menikah. Nah, lalu apa saja sih unsur
        dari kebudayaan tersebut dan apakah kebudayaan memiliki jenis yang
        berbeda? Artikel satu ini akan membahas secara singkat mengenai
        <a name="pengertian"></a>
        pengertian, jenis serta unsur kebudayaan. Simak hingga akhir artikel ya!
        
      </p>
      
        <h3>Pengertian</a></h3>
      <p>
        Secara etimologi, kata culture atau budaya berasal dari bahasa latin
        yaitu colere yang berarti mengolah atau mengerjakan. Kata culture dalam
        bahasa inggris juga dapat diartikan sebagai kultur dalam bahasa
        Indonesia dan berarti kebudayaan. Selain secara etimologi, beberapa ahli
        turut mengemukakan pendapatnya mengenai pengertian kebudayaan. Berikut
        pendapat para ahli mengenai pengertian kebudayaan:
      </p>

      <ol>
        <li>
          <b>E.B Taylor </b>
          <p>
            Menurut Taylor, kebudayaan merupakan hal kompleks yang mencakup
            beberapa hal di dalamnya seperti kepercayaan, kesenian, hukum,
            moral, adat istiadat serta kemampuan yang dapat diperoleh manusia
            sebagai bagian dari kelompok masyarakat tersebut.

            <li>
              <b>Ki Hajar Dewantara</b>
              <p>
                Kebudayaan menurut Ki Hajar Dewantara adalah buah budi dari
                manusia yang muncul karena adanya hasil alam serta kodrat
                masyarakat. Kebudayaan menurut Ki Hajar Dewantara juga bentuk
                dari kejayaan dari masyarakat yang mampu mengatasi
                kesulitan-kesulitan serta menjadi awal dari munculnya tata
                tertib di masyarakat.
              </p>
            </li>

            <li>
              <b>Selo Seomardjan dan Soelaeman Somardi </b>
              <p>
                Menurut Selo dan Soelaeman, kebudayaan merupakan seluruh hasil
                <a name="cirikebudayaan">
                karya, rasa, serta cipta dari masyarakat.
              </p>
            </li>
          </p>
        </li>
      </ol>

      <h3>Ciri-ciri Kebudayaan</a></h3>
      <p>Kebudayaan dapat dikenali melalui ciri-cirinya sebagai berikut:</p>
      <ol>
        <li>
          Budaya yang hadir di masyarakat akan dipelajari oleh generasi
          selanjutnya.
        </li>
        <li>
          Budaya dapat disampaikan oleh setiap individu pada individu maupun
          kelompok lain, serta diwariskan dari generasi ke generasi selanjutnya.
        </li>
        <li>
          Budaya memiliki sifat yang dinamis, artinya budaya dapat berubah
          sepanjang waktu.
        </li>
        <li>
          Budaya memiliki sifat selektif yang dapat mencerminkan pola perilaku
          serta pengalaman manusia secara terbatas.
        </li>
        <li>
          Walaupun kebudayaan setiap daerah berbeda, budaya memiliki unsur yang
          saling berkaitan.
        </li>
        <li>
          Masyarakat yang memiliki kebudayaan tersebut akan beranggapan
          etnosentrik atau menganggap bahwa budayanya sebagai budaya yang
          terbaik dan menilai budaya masyarakat hanyalah budaya standar.
        </li>
        <li>
          Budaya memiliki unsur kepercayaan di dalamnya yang dipercayai oleh
          anggota masyarakat yang memiliki kebudayaan tersebut.
        </li>
        <li>
          Dalam kebudayaan ada bahasa serta ciri khas dari setiap daerah yang
          memiliki budaya tersebut.
        </li>
        <li>
          Budaya merupakan produk yang diciptakan oleh manusia atau sekelompok
          manusia.
        </li>
        <li>
        <a name="fungsikebudayaan">  
          Budaya meliputi obyek materi yang diwujudkan melalui teknologi, serta
          meliputi sikap, nilai dan pengetahuan.
        </li>
      </ol>

      <h3>Fungsi Kebudayaan</a></h3>
      <p>
        Kebudayaan memiliki beberapa fungsi yang hadir dan dapat dirasakan oleh
        masyarakat. Fungsi utama kebudayaan sendiri adalah untuk mempelajari
        warisan dari nenek moyang, kemudian generasi selanjutnya perlu meninjau,
        apakah warisan tersebut perlu diperbaharui atau tetap dilanjutkan dan
        apabila ditinggalkan maka kebudayaan tersebut dapat rusak. Budaya maupun
        unsur-unsur yang ada di dalamnya terikat oleh waktu serta bukan menjadi
        kuantitas yang bersifat statis. Budaya pun akan tetap berubah baik
        secara lambat maupun cepat. Berikut adalah beberapa fungsi dari
        kebudayaan:
      </p>
      <ol>
        <li>
          Kebudayaan dapat meningkatkan rasa nasionalisme masyarakat yang
          memiliki budaya tersebut.
        </li>
        <li>
          Kebudayaan dapat menimbulkan rasa toleransi serta rasa empati dari
          masyarakat.
        </li>
        <li>
          Masyarakat yang memiliki budaya tersebut, akan menghargai satu sama
          lain.
        </li>
        <li>
          Kebudayaan dapat dijadikan sebagai sebuah sarana untuk dapat menjalin
          sosialisasi.
        </li>
        <li>Kebudayaan juga berfungsi sebagai media belajar.</li>
        <li>
          Kebudayaan berfungsi sebagai penentu batas, artinya kebudayaan dapat
          menciptakan perbedaan yang membuat setiap kelompok masyarakat unik dan
          membedakannya dengan kelompok masyarakat lain.
        </li>
        <li>
          Budaya berfungsi untuk memberikan rasa identitas pada anggota
          kelompoknya.
        </li>
        <li>
          Budaya berfungsi untuk memfasilitasi lahirnya komitmen pada suatu hal
          yang lebih besar dari kepentingan individu anggota kelompok masyarakat
          tersebut.
        </li>
        <li>
          Kebudayaan berfungsi untuk dapat meningkatkan kemantapan pada sistem
          sosial di masyarakat.
        </li>
        <li>
          Kebudayaan bertindak sebagai sebuah mekanisme sebagai pembuat makna
          <a name="jeniskebudayaan">
          maupun kendali yang dapat menuntun dan membentuk sikap dan perilaku
          individu.
        </li>
      </ol>

      <h3></a>Jenis-jenis Kebudayaan</h3>
      Kebudayaan terbagi dalam beberapa jenis sesuai dengan sifatnya, wujudnya,
      dan lingkup persebarannya. Berikut penjelasan mengenai jenis-jenis
      kebudayaan:

      <ol>
        <li><b>Jenis Kebudayaan Berdasarkan Sifatnya</b></li>
        <ul>
          <li><b>Kebudayaan Subjektif</b></li>
          Kebudayaan subjektif merupakan faktor nilai, perasaan, idealisme yang
          apabila disimpulkan maka dapat disebut sebagai faktor batin yang ada
          pada kebudayaan tersebut.
          <p></p>
          <li><b>Kebudayaan Objektif</b></li>
          Kebudayaan objektif merupakan faktor lahiriah yang hadir dari sebuah
          kebudayaan dan berupa teknik, lembaga sosial, pengajaran, seni suara,
          seni rupa, seni sastra hingga upacara yang menggunakan budi bahasa.
        </ul>
        <p></p>
        <li><b>Jenis Kebudayaan Berdasarkan Wujudnya</b></li>
        <ul>
          <li><b>Kebudayaan Material</b></li>
          Kebudayaan material ini mengacu kepada seluruh ciptaan manusia yang
          nyata serta konkret. Di dalamnya termasuk temuan yang dihasilkan oleh
          penggalian arkeolog seperti senjata, perhiasan hingga mangkuk dari
          tanah liat. Kebudayaan material pun mencakup barang-barang lain selain
          dari temuan arkeologi, seperti pesawat terbang, pakaian, televisi,
          gedung pencakar langit, stadion olahraga hingga mesin cuci.
          <p></p>
          <li><b>Kebudayaan Immaterial</b></li>
          Jenis kebudayaan immaterial berupa ciptaan yang abstrak dan diwariskan
          oleh pendahulunya ke generasi selanjutnya. Contohnya seperti lagu,
          tarian tradisional, dongeng, hingga cerita rakyat.
        </ul>

        <p></p>
        <li><b>Jenis Kebudayaan Berdasarkan Lingkup Persebarannya</b></li>
        <ul>
          <li><b>Kebudayaan Daerah</b></li>
          Kebudayaan dapat berupa cara berperilaku, pola pikiran hingga cara
          bertindak dari anggota kelompok masyarakat yang memiliki kebudayaan
          tersebut. Kebudayaan daerah dapat dibatasi oleh wilayah administratif
          daerah tersebut atau demografinya. Wilayah demografis tersebut menjadi
          batasan budaya lokal, namun seiring dengan perkembangan batasan
          wilayah kebudayaan daerah ini menjadi tidak terbatas akibat dari
          persebaran penduduk yang tidak merata.
          <p></p>
          <li><b>Kebudayaan Lokal</b></li>
          Kebudayaan lokal bergantung pada aspek ruang. Hal ini dapat dilihat
          melalui ruang pada perkotaan yang hadir sebagai budaya lokal perkotaan
          tersebut, atau daerah tertentu pada bagian perkotaan yang terpengaruh
          oleh budaya yang dibawa oleh pendatang. Pada kebudayaan lokal, ada
          pula kebudayaan dominan yang berkembang yaitu budaya lokal asli pada
          kota atau daerah tersebut.
          <b><i>Koentjaraningrat</i></b>
          berpendapat bahwa "Budaya lokal berkaitan dengan golongan manusia yang
          terikat oleh kesadaran serta identitasnya akan kesatuan kebudayaan
          asli di tempatnya." Dalam hal ini yang disebut sebagai kebudayaan
          lokal adalah bahasa sebagai ciri khasnya.
          <p></p>
          <li><b>Kebudayaan Nasional</b></li>
          Kebudayaan nasional merupakan kebudayaan dari akumulasi dari budaya
          yang hadir daerah-daerah. Ada berbagai macam wujud kebudayaan nasional
          dan dapat dilihat secara umum apabila diperhatikan dengan cermat, maka
          dapat diketahui bahwa terdapat persebaran besar yang terjadi antar
          kebudayaan di satu daerah dan daerah lainnya. Namun, keragaman budaya
          tersebut yang menjadikan suatu bangsa memiliki jati dirinya.

          <p>
            Pada kebudayaan nasional, terdapat beberapa persebaran. Berikut
            penjelasannya:
          </p>
          <ol>
            <li>
              Rumah Adat, merupakan rumah yang memiliki ciri khas dan umumnya
              terdapat di masing-masing daerah. Setiap daerah memiliki rumah
              adat dengan ciri khas yang berbeda-beda dan pemaknaan yang berbeda
              pula.
            </li>
            <li>
              Upacara Adat, merupakan sebuah tradisi yang dilaksanakan secara
              turun temurun dengan teratur serta tertid sesuai dengan kebiasaan
              masyarakat setempat. Berupa rangkaian aktivitas sebagai wujud
              ungkapan terimakasih atas suatu hal, sesuai dengan sistem
              kepercayaan masyarakat. Upacara adat memiliki nilai yang
              universal, suci, bernilai sakral religious dan dilakukan secara
              turun temurun.
            </li>
            <li>
              Tarian, di setiap daerah memiliki tarian adat yang berbeda dan
              akan ditarikan dalam upacara atau peringatan khusus saja. Contoh
              tarian sebagai persebaran kebudayaan nasional adalah tarian ranup
              lampuan di Aceh.
            </li>
            <li>
              Lagu, Indonesia memiliki banyak lagu daerah dalam bahasa daerahnya
              masing-masing. Setiap lagu daerah memiliki makna serta pesannya
              tersendiri. Selain itu, setiap bangsa juga memiliki lagu nasional
              yang berfungsi untuk meningkatkan persatuan negaranya.
            </li>
            <li>
              Musik, musik-musik tradisional umumnya berupa instrumen
              menggunakan alat musik khusus di daerah tersebut. Contohnya
              seperti angklung di Jawa Barat atau gamelan di Jawa Tengah.
            </li>
            <li>
              Pakaian Adat, sama halnya dengan persebaran budaya nasional
              lainnya. Pakaian adat juga memiliki ciri khas pada daerah yang
              memiliki pakaian adat tersebut dan hanya dikenakan dalam upacara
              khusus. Pakaian adat juga dipengaruhi oleh faktor agama di daerah
              masing-masing. Contohnya seperti kebaya di Jawa, ulos di Sumatera
              Utara, ulee balang di Aceh, kain cual di Bangka Belitung.
            </li>
          </ol>
        </ul>
      </ol>

