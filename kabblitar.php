    <h3>Candi Sawentar</h3>
        <div align="center">
          <img 
        style="width: 75%;"
        src="imgsrc/CandiSawentar.jpg" alt="CandiSawentar">
        </div>
        <p>Secara administratif Candi Sawentar terletak di Dukuh Centong, Desa Sawentar, Kecamatan Kanigoro, Kabupaten Blitar. Untuk mencapainya dapat melalui jalan utama Blitar – Malang. Melewati rute ini dari kota Blitar setelah sampai Kantor Kelurahan Bence belok ke selatan melalui jalan desa sejauh 2 kilometer. Candi Sawentar tampak berdiri di tepi jalan desa, Candi Sawentar II berjarak 100 meter ke barat. <br>
        Ditinjau dari sudut kebudayaan, bangunan Candi Sawentar memperlihatkan persamaan arsitektur serta ornamen dengan bangunan candi jaman Singosari yaitu Candi Kidal (Kempers, 1959; Team: 1976:37). Sedangkan menurut Hariani Santiko, Candi Sawentar merupakan candi dengan gaya Singasari yaitu candi yang memiliki bagian-bagian kaki-tubuh-atap candi dengan ruangan candi yang dapat dimasuki dari satu pintu yang biasanya ada di sisi barat. Bentuknya terlihat ramping dan lebih tinggi dari ukuran sebenarnya karena atap candi menjulang tinggi, lapisan-lapisan atap tidak terlihat dengan jelas yang kemudian diberi puncak kubus yang memanjang ke atas (Kusumajaya: 2007: 82). <br>
        Sampai saat ini masalah penjamanan Candi Sawentar masih ada dua pendapat yang berbeda. Pendapat pertama bahwa candi itu berasal dari awal abad XIII atau merupakan hasil kesenian periode Singosari (Sulaiman, 1976:26). Hal ini didasarkan atas arsitekturnya yang hampir sama dengan Candi Kidal yan gdidirikan sekitar 1260 Masehi sebagai tempat pendharmaan Anusapati, atau 12 tahun setelah kematiannya. Pendapat kedua mengatakan bahwa Candi Sawentar berasal dari periode awal Majapahit (Krom, 1923:292; Stutterheim, 1948:77). Hal ini didasarkan adanya hiasan Surya Majapahit yang diduga sebagai lambang Kerajaan Majapahit (Mursitawati, 1987: 88) yaitu pada batu penutup lubang cungkupnya terdapat pahatan lingkaran dengan sudut-sudut yang merupakan pancaran sinar sebanyak dua puluh tujuh. Di tengah lingkaran tampak pahatan tokoh menunggang kuda memakai mahkota, di sekitar kepala terdapat siras cakra. Tokoh tersebut digambarkan dengan tangan kanan diangkat agak ke samping memegang tali kendali, tangan kiri ke depan juga memegang kendali. Pakaian yan gdikenakan sebatas lutut, memakai selendang pada pinggang. Kaki kanan terlipat sedang kaki kiri tidak terlihat karena tertutup badan kuda. Kuda digambarkan berbadan agak kecil, mempunyai telinga panjang seperti telinga kelinci, ekor menjuntai ke bawah. Posisi kaki kuda menekuk ke belakang seolah-olah hendak melompat. Di bawah kuda terdapat hiasan berupa sulur-suluran (Mursitawati, 1987: 11-12). <br>
        Keletakan relief Surya Majapahit pada batu penutup cungkup mempunyai arti dan maksud tertentu. Menurut Stutterheim relief pada batu penutup cungkup melukiskan sesuatu yang betalian erat dengan kedewaan dan dengan demikian mempunyai peranan yang berhubungan dengan keagamaan. Seperti telah dijelaskan sebelumnya di engah relief Surya Majapahit ada penggambaran tokoh menunggang kuda. Penggambaran ini menurut Sutterheim berhubungan dengan seorang tokoh dewa. Dalam alam pikiran Hindu, kuda sering dihubungkan dengan Indra, Surya dan Wisnu. Menurut Van Stein Callenfels tokoh menunggan kuda dihubungkan dengan Indra dengan kuda uccaiśrawa dan bertempat tingggal di atas meru seperti yang terdapat dalam cerita Samudramantana. Tetapi Stutterheim menolak dengan mengatakan bahwa hal tersebut tidak ada kaitannya dengan Samudramantana, ia mengidentifikasi tokoh menunggang kuda dengan Surya. Sedangkan N.J. Krom dengan membandingkan relief tokoh penunggang kuda pada batu penutup cungkup. Candi Bangkal bahwa tokoh penunggang kuda adalah Kalki. Sebagai dewa penyelamat Kalki digambarkan mengendarai seekor kuda putih dengan pedang terhunus dan bercahaya seperti binatang bersayap. Pendapat Krom lebih dapat diterima, karena Kaliki adalah awatara ke sepuluh Wisnu. Dalam kitan-kitab Veda, Wisnu dianggap sebagai dewa yang tertinggi kedudukannya. Wisnu dikatakan mempunyai sifat sebagai matahari dan telah mengunjungi tujuh bagian dunia serta mengedari dunia tiga langkah (triwikrama). Sebagai dewa, Wisnu menjelma dalam tiga wujud yaitu api, halilintar, dan sinar matahari yang ada di dunia. Pemujaan pada Wisnu dalam bentuk matahari sering disebut dengan nama surya narayana (Basori, 1992: 4-5). Dihubungkan dengan yoni yang memiliki relief garuda dengan posisi berada di bawah batu penutup cungkup dan relief yang terdpaat di batu penutup cungkup, kemungkinan Candi Sawentar bersifat Wisnuistis. <br>
        Candi Sawentar tercantum dalam kitab Nagarakrtagama pupuh LXI bait 2 yang disebut dengan Lwa Wentar. Bunyi lengkap kitab Nagarakrtagama pupuh LXI bait 2 yaitu: ndan ri śakha tri tanu rawi rin weśaka, śri nātha mūja mara ri palah sabhrtya, jambat sin ramya pinaranirān/lātnlitya, ri lawn wĕntār mmanuri balitar mwan jimbe (Pigeaud, 1960: 46). Artinya: tahun saka tiga badan dan bulan (1283) waisaka baginda raja berangkat menyekar ke Palah dan mengunjungi Jimbe untuk menghibur hati di Lawang Wentar Blitar menentramkan cita (Muljana, 2007:380). <br>
        Hingga saat ini Candi Sawentar tidak diketahui dibangun sebagai tempat pendharmaan siapa, mengingat dalam Nagarakrtagama maupun prasasti tidak pernah menyebutkannya.
        (Lap. Usulan Penetapan CB, 2009) <a name="arcaganesaboro"></a>
        </p> <br>
  
    <h3>Arca Ganesa Boro</a></h3>
        <div align="center">
          <img 
          style="width: 409.2px;"
          src="imgsrc/ArcaGaneshaBoro.jpg" alt="ArcaGaneshaBoro">
        </div>    
    
        <p>Arca Ganesa Boro yang terletak di dusun Boro Desa Tuliskriyo Kec. Sanankulon,Kab Blitar cukup unik karena mempunyai dua wujud, pada bagian depan terpahat wujud Ganesa sedai pada bagian belakang terpahat wujud Mahakala. Ukuran Arca adalah tinggi 170 cm,lebar 113 cm, panjang 113 cm. Arca tersebut terbuat dr batu andesit.
        Ganesa Boro digambarkan dalam posisi duduk bersila seperti bayi dengan kedua telapak kaki saling bertemu, diatas landasan bulat berhias untaian tengkorak. Pemakaian atribut tengkorak ini menunjukkan adanya aliran tertentu yang berkembang pada waktu itu yaitu TANTRAYANA. Wajah Ganesa digambarkan dengan mata setengah terpejam. Salah satu gadingnya patah sehingga disebut EKANDANTA yaitu bergading satu. Belalai menjulur ke dalam mangkok modaka yg dipegang tangan kiri depan. Ganesa boro bertangan empat, kedua tangan depan terletak diatas lutut,sebelah kiri memegang mangkuk dan sebelah kanan memegang memegang patahan gading. Tangan belakang sebelah kiri memegang parasu/kapak dan aksamala/tasbih sebelah kanan. Perhiasan yang dipakai sangat indah dan terkesan mewah meliputi mahkota,gelang lengan,gelang tangan,dan pakaian yang memakai uncal. Di bagian bawah ARCA terpahat kronogram yang berbunyi Hana Ghana Hana Bumi yang jika ditulis angka menjadi 1611. Namun karena tahun yg terpahat tahun saka maka angka tersebut harus dibaca dari kanan menjadi 1161 saka. Di dalam mitologi agama Hindu Ganesa anak dr Siwa dan Parwati.
          <p>
        Secara umum Ganesa dikenal sebagai dewa ilmu pengetahuan,hal ini digambarkan dengan belalai yang menghisap sesuatu dari mangkuk ibarat orang yang selalu haus akan ilmu pengetahuan. Ganesa boro juga dikenal sebagai dewa penghalau rintangan.oleh karena itu arca Ganesa sering diletakkan diperempat jalan,ditempat tempat angker,dan dipinggiran sungai. Sehingga sangat masuk akal apabila arca Ganesa Boro ini berada dipinggir sungai Brantas. Yang konon Ganesa boro berasal dari Kekunaan desa Jimbe yang dipindahkan ke kadipaten Blitar,tiba tiba saja arca ini menghilang dan berpindah secara gaib ke dusun Boro, desa Tuliskriyo, kab Blitar.
        Dan ringkasan untuk Kekunaan Jimbe merupakan situs peninggalan kerajaan Singosari, sehingga arca Ganesa Boro juga merupakan arca peninggalan dari era kerajaan Singosari. Dan memang jika ditilik dari kronogram nya arca Ganesa Boro adalah arca peninggalan dari era kerajaan Singosari yang berdiri 17 tahun sebelumnya pada 1141 saka. Terlepas dari unsur mitos, sepertinya peletakan arca Ganesa Boro merupakan salah satu upaya legitimasi kerajaan Singosari (era Raja Anusapati) atas bandar-bandar disekitar Sungai Brantas yang dulunya dikuasai Kediri.<a name="arcagaprang"></a> </p>  <br>
  
