        <p>Kelompok Arca Gaprang lokasinya terletak di Desa Gaprang, Kecamatan Kanigoro, Kabupaten Blitar, Propinsi Jawa Timur. Arca Gaprang terbuat dari bahan Batu Andesit.
        Terdapat 2 (dua) arca yang saling berhadaan dalam posisi jongkok. Arca Gaprang digambarkan dengan ciri-ciri badan besar seperti raksasa, mempunyai alat kelamin yang berukuran besar. Wajah arca digambarkan dengan wajah yang seram, mata melotot dan mulut meringis menunjukan taringnya, rambut terlihat lurus kebelakang sampai ke punggung.
        <p>
        Dari perwujudannya, terutama dari penggambaran alat kelamin yang sangat besar, dapat diperkirakan bahwa arca tersebut ada hubungannya dengan masalah kesuburan atau bisa jadi karena wajahnya adalah wajah sosok seorang raksasa yang dahsyat. Namun dapat juga penggambaran arca hindu yang disebut tantrayana.
