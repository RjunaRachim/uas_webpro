        <p>Candi Simping dikenal pula dengan Candi Sumberjati terletak di Dusun Mrajan, Desa Sumberjati Kecamatan Kedemangan, Kabupaten Blitar.
        Keberadaan Situs Candi Simping ini memiliki nilai penting yang dapat memberikan gambaran sejarah mengenai peristiwa penting yang terjadi pada masa lalu dan berkaitan dengan Tokoh Krtarajasa Jayawardhana, Raja pertama kerajaan Majapahit.
        <p>
        Situs Candi Simping merupakan tempat pendharmaan Raden Wijaya, raja pertama Kerajaan Majapahit yang wafat tahun 1309 Masehi. Di candi ini dulu pernah diketemukan arca Harihara, Dewa gabungan Siwa dan Wisnu sebagai penggambaran Raden Wijaya. Arca Harihara yang masih dalam kondisi bagus dan utuh ini sekarang disimpan di Museum Nasional Republik Indonesia, Jakarta.
        Kitab Negarakertagama mencatat bahwa Raja Hayam Wuruk pernah merenovasi candi ini pada tahun 1285 Saka (1363 M). (Lap. Verifikasi ODCB di Kab. Blitar) <a name="candisawentar"></a>
