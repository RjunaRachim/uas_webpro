        <h3>Candi Penataran</h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/CandiPenataran.jpg" alt="CandiPenataran">
        </div>
        
        <p>Candi Penataran adalah sebuah candi berlatar belakang Hindu (Siwaitis) yang terletak Desa Penataran Kecamatan Nglegok, tepatnya di lereng barat daya Gunung Kelud, di sebelah utara Blitar jarak tempuh dari pusat kota kurang lebih 12 Km. Komplek Candi ini merupakan yang terbesar di Jawa Timur.
        <p>
        Candi ini di bangun dari Kerajaan Kediri dan dipergunakan sampai dengan Kerajaan Majapahit, Seperti pada umumnya relief candi di Jawa Timur yang dipahat berdasarkan analogi romantika hidup tokoh yang didharmakan di tempat tersebut, relief Ramayana dengan tokoh Rama dan Shinta, dan relief Krisnayana dengan tokoh Krisna dan Rukmini, yang dipahatkan pada dinding candi Penataran dapat dikatakan mirip dengan kisah Ken Arok dan Ken Dedes.
        <p>
        Keberadaan Wisata Candi Penataran sangat membantu mengangkat perekonomian masyarakat di Sekitar Candi, banyaknya kios-kios penjual sangat memudahkan wisatawan untuk mencari oleh-oleh, selain itu untuk lebih memikat Wisatawan Pemerintah Kabupaten Blitar juga membangun Fasilitas Wisata lain yaitu pemandian atau kolam renang yang jaraknya tiak terlalu jauh dari Kawasan Candi Penataran.<a name="candisimping"></a></p>
