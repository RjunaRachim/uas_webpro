    <h3>Perpustakaan Proklamator Bung Karno</a></h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/perpustakaan.jpeg" alt="perpustakaan">
        </div>
        
        <p>Perpustakaan Proklamator Bung Karno adalah perpustakaan kepresidenan, termasuk jenis perpustakaan khusus yang bertugas meningkatkan budaya dan peradaban Indonesia melalui pemikiran dan tindakan orang besar. Dinamakan perpustakaan Proklamator Bung Karno, karena:<br>
        <ul>
        <li>Adanya keinginan yang kuat dalam mengembangkan Nasionalisme Indonesia dimasa depan terutama di dalam menghadapi ideologi globalisme yang pasti akan menghampiri bangsa ini.</li>
        <li>“Kegandrungan” akan sosok Bung Karno dengan pemikiran dan cita-citanya yang sangat luar biasa dalam meletakkan fondasi bagi kehidupan kebangsaan Indonesia.</li>
        <li>Berbagai aktivitas yang dipusatkan di Perpustakaan Proklamator Bung Karno sebagai salah satu pusat kajian tentang Nasionalisme Indonesia.</li>
        <li>Tujuan utamanya adalah berusaha mewujudkan gagasan agar rakyat Indonesia yang datang berkunjung berziarah ke makam Bung Karno dapat diajak untuk mewarisi, mengkaji, merenung, mereaktualisasikan cita-cita dan pemikiran Bung Karno serta pemahaman yang memadahi tentang sejarah terbentuknya Negara Kesatuan Republik Indonesia (Kisdarjono & Tim, 2010: 9).</li>
        </ul>  
        <b>Sejarah</b><br>
        Perpustakaan Proklamator Bung Karno adalah Unit Pelaksana Teknis (UPT) Perpustakaan Nasional Republik Indonesia, yang pembangunannya tidak terlepas dari Pemerintah Kota Blitar, diresmikan pada 3 Juli 2004 oleh Presiden Megawati Sukarno Putri. Terletak strategis berada dalam satu kawasan dengan makam Proklamator Bung Karno di Jl. Kalasan No.1 Kota Blitar.
        <a name="makamaryo"></a></p>
        <br>

    <h3>Makam Aryo Blitar</a></h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/makamadipatiaryo.jpeg" alt="makamAryo">
        </div>
        
        <p>Terletak di Kelurahan Blitar, Kecamatan Sukorejo Kota Bitar, kira-kira 2 km dari arah barat kota. Makam ini ramai pada saat bulan Asyura dan juga setiap malam jum`at legi. Banyak orang datang ke makam tersebut untuk mendapatkan informasi tentang Aryo Blitar dan juga berkah.
        <p>Merupakan tempat peristirahatan terakhir Adipati Aryo Blitar yang bernama asli Gusti Soedomo yang diangkat menjadi K.G.P. Adipati Balitar bergelar Adipati Niti Soewarno (K.G.P. Adipati Haryo Balitar l) paman dari Hamengku Buwono I (pangeran Mangkubumi) raja Jogjakarta. Adipati Aryo Blitar diyakini sebagai pendiri (pepunden) Blitar.            
