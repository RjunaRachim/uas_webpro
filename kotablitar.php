    <h3></a>Makam Bung Karno</h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/makambungkarno.jpeg" alt="makam">
        </div>
        
        <p>Makam Bung Karno / Makam Soekarno salah satu daya tarik wisata di Blitar yang bahkan menjadi ikon Kota Blitar. Ribuan wisatawan datang ke Blitar setiap harinya untuk berziarah di Pusaranya.
        Menjelang pemilu, tidak sedikit tokoh politik nasional yang datang untuk berziarah ke makam proklamator Indonesia ini.
        <p>Sejarah mungkin banyak yang bertanya-tanya mengapa Bung Karno dimakamkan di Blitar? Padahal beliau pernah berwasiat untuk dimakamkan di Kebun Raya Bogor.
        Kota Blitar dipilih untuk mengurangi pengaruh Bung Karno di daerah ibukota. Ini tidak lepas dari masalah politik yang cukup pelik saat itu. Bahkan ada Keppres No 44 Tahun 1970 untuk menentukan lokasi pemakaman Bung Karno ini,
        Arsitektur
        Desain bangunan utama menggunakan arsitektur khas jawa berupa bangungan Joglo yang dikombinasikan dengan atap sisik ikan khas sunda. 
        <p>Selain makam Bung Karno, terdapat pula makam Ayah dan Ibu Bung Karno. Dalam komplek makam ini terdapat 3 bagunan, satu merupakan makam dimana tempat para peziarah, selain itu juga terdapat mushola di bagian baratnya, serta sebuah bangunan agak lapang dengan maket kawasan makam pada bagian timurnya. 
        Dulunya, terdapat sekat kaca di sekeliling tiang penyangga Joglo. Sejak era Gus Dur, kaca penyangga tersebut dilepas hingga kini sehingga siapapun bisa langsung berdoa di makam Bung Karno yang merupakan Bapak Proklamasi Indonesia ini.
        <a name="perpusbk"></a>
      </p>
        
