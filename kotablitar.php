      <h1 style="font-size: 34; text-align: center;"><b>DESTINASI WISATA & SITUS KEBUDAYAAN</b><a name="istanagebang"></a></h1>
      
      <h3>Istana Gebang Blitar</h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/istanagebang.jpg" alt="istanagebang">
        </div>
        
        <p>Istana Gebang merupakan rumah keluarga Bung Karno yang berdiri diatas lahan seluas 1,8 hektar. Terdiri dari sebuah rumah induk dengan delapan bangunan rumah lain. Awalnya rumah ini merupakan pejabat bBellanda yang kemudian dibeli oleh ayah Bung Karno, Soekemi Soestrohardjo saat mengajar di SMA di Kota Blitar tahun 1917. Diperkirakan rumah tersebut dibangun tahun 1914 saat Bung Karno masih bersekolah di HBS Surabaya.
        Istana Gebang sekarang menjadi obyek wisata museum di Kota Blitar dan merupakan salah satu tempat wisata dekat dengan Stasiun Blitar. Masih tersimpan dengan baik beberapa barang peninggalan Bung Karno dan keluarga.
        <p>
        Dua kamar tidur yang sering ditempati Bung Karno saat liburan sekolah maupun kuliah, bahkan saat menjabat sebagai presiden. Saat ini selain berfungsi sebagai museum peninggalan Bung Karno, Istana ini juga menjadi tempat perayaan haul Bung Karno dan pagelaran kesenian.
        <a name="monumenpeta"></a>
        </p>
        <br>

      
      <h3>Monumen Peta</h3>
        <div align="center">
          <img style="width: 75%;" src="imgsrc/monumenpeta.jpg" alt="monumen">
        </div>
        
        <p>Monumen Peta di Blitar dibangun untuk memperingati pemberontakan PETA (Pembela Tanah Air) melawan tentara Jepang pada 14 Februari 1945 yang dipimpin oleh Soedanco Soepriyadi.
        Monumen PETA Blitar dibangun di depan eks markas PETA, yaitu di Jl. S. Supriyadi, Kota Blitar. Monumen ini kini berada dalam kawasan SMP Komplek di Kota Blitar yang rencananya akan dijadikan Monumen PETA setelah sekolah di relokasi. Namun sampai saat ini belum diketahui kapan hal tersebut akan di realisasikan.
        <p>
        Monumen Peta di Blitar ini diresmikan oleh M Basofi Soedirman yang merupakan Gubernur Jawa Timur pada 14 Februari 1998, yang kemudian diperbarui pada tahun 2008 sehingga bentuknya seperti sekarang ini. 5 Tokoh Pemberontakan PETA di Blitar.
        Adapun 7 tokoh yang diabadikan dalam bentuk patung di monumen ini adalah:
        <ol>
            <li>Shodancho Soeprijadi,</li>
            <li>Chudancho dr Soeryo Ismail,</li>
            <li>Shodancho Soeparjono,</li>
