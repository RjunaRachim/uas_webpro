    <h3>Monumen Potlot Taman Makam Pahlawan Raden Wijaya</a></h3>
        <div align="center">
          <img 
          style="width: 75%;"
          src="imgsrc/MonumenPotlot.jpeg" alt="MonumenPotlot">
        </div>
        
        <p>Sebelum Indonesia mencapai kemerdekaannya pada tanggal 17 Agustus 1945, terdapat fakta yang mengejutkan bahwa bendera merah putih telah dikibarkan enam bulan sebelumnya. Lokasi pertama kali pengibaran bendera merah putih ini terjadi di Kota Blitar, tepatnya di Monumen Potlot.
        <p>Monumen Potlot tegak dengan teguh sebagai pusat perhatian di Taman Makam Pahlawan (TMP) Raden Wijaya, Blitar. Meskipun berada di bagian belakang area, bentuknya yang tajam dan menjulang seperti ujung pensil, berhasil menarik perhatian siapa pun yang memasuki lingkungan makam.
        <p>Sejarawan Blitar, Bambang In Mardiono, dalam bukunya yang berjudul “Bunga Rampai Sejarah Blitar,” mengungkapkan alasan mengapa monumen ini dinamai Potlot. Ini karena mayoritas pejuang PETA pada saat itu adalah pelajar berusia antara 14 hingga 16 tahun.
        <p>Jadi, istilah potlot atau pulpen, menjadi simbol dari alat tulis yang digunakan oleh para pelajar tersebut. Meskipun masih muda, mereka berjuang dengan gigih demi kemerdekaan bangsa.
