    <h3></a>Pesanggerahan Djojodigdan</h3>
            <div align="center">
              <img 
              style="width: 75%;"
              src="imgsrc/pesanggerahan.jpg" alt="pesanggerahan">
            </div>
            
        <p>Pesanggrahan Djojodigdan merupakan rumah tinggal R.NG. Bawadiman Djojodigdo. Beliau adalah Patih Blitar yang mendampingi Bupati Blitar kedua, KPH. Warsokoesoemo. Djojodigdo, anak dari RT. Kartodiwirjo dan cucu dari RT. Projo Bupati Kulon Progo. Pada saat perang Diponegoro RT. Kartodiwirjo adalah salah satu pemimpin pasukan Pangeran Diponegoro yang sangat handal di medan pertempuran sehingga menjadi incaran pemerintah kolonial Belanda. RT. Kartodiwirjo tewas dalam pertempuran melawan tentara kolonial Belanda dan dimakamkan di Magelang. R.NG. Bawadiman Djojodigdo dan ibunya kemudian mengungsi menghindari tangkapan tentara Belanda. Pada masa pengungsian mereka berpindah tempat dan sampailah ke daerah Blitar (wawancara dengan Bpk. Handojo Prijo Soetedjo, S.Kom).
        <p>Keberadaan R.NG. Bawadiman Djojodigdo dengan kecerdasan dan keilmuan yang tinggi menghantarkan sosok menantu Bupati Berbek R.Ng. Pringgo Digdo ini menjadi Patih Blitar pada tanggal 8 September 1877 (Gudel,2006;65). Dalam melaksanakan tugas, sosok Djojodigdo dikenal selalu dekat dengan rakyat dan dari cerita tutur masyarakat Blitar, Djojodigdo merupakan sosok yang mempunyai ilmu kadigjayaan yang sangat luarbiasa. Diceritakan, ketika terjadi bencana letusan Gunung Kelud tahun 1901 dimana lahar sampai menerjang pusat Kota Blitar, cemeti sakti milik beliau mampu membelokkan lahar tersebut. Karena jasanya ini Ratu Belanda Wilhelmina II memberikan hadiah berupa Gold Medals (wawancara dengan Bpk. Hanjo Prijo Soetedjo, S.Kom.)
        <p>Rumah kediaman Djojodigdo dibangun bersamaan dengan pembangunan kantor Bupati tahun 1875. Awalnya tanah ini cukup luas berupa tanah perdikan yang diberikan kepada Djojodigdo sebagai seorang Patih. Saat ini areal kediaman Djojodigdo hanya 2,7 Ha. Rumah ini pun telah mengalami pembangunan besar-besaran yang dilakukan oleh Bupati Rembang R. Mas Adipati Arjo Djajaningrat, anak dari Patih Djojodigdo. Kediaman Patih Djojodigdo yang saat ini dikenal dengan nama Pesanggrahan Djojodigdan dikelola oleh yayasan keluarga Djojodigdo yang diketuai oleh Bpk. Buke Abubakar. (Lap. Inventarisasi ODCB di Kota Blitar Tahap II 2015).
